"""News digest talent — aggregate RSS feeds into a dated HTML file.

Config in settings.json under "news_digest":
  {
    "output_dir":           "",          // defaults to ~/talon_news/
    "max_stories_per_feed": 8,
    "max_age_hours":        24,
    "feeds": [
      {"name": "Reuters",  "url": "https://...", "category": "General"},
      ...
    ]
  }

Trigger phrases:  "generate morning news digest", "morning briefing",
                  "daily news", "collect news", etc.
"""
